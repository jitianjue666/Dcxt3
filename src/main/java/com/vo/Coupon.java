package com.vo;

public class Coupon {
    private Integer couponid;

    private Integer spid;

    private Double frommoney;

    private Double minusmoney;

    public Integer getCouponid() {
        return couponid;
    }

    public void setCouponid(Integer couponid) {
        this.couponid = couponid;
    }

    public Integer getSpid() {
        return spid;
    }

    public void setSpid(Integer spid) {
        this.spid = spid;
    }

    public Double getFrommoney() {
        return frommoney;
    }

    public void setFrommoney(Double frommoney) {
        this.frommoney = frommoney;
    }

    public Double getMinusmoney() {
        return minusmoney;
    }

    public void setMinusmoney(Double minusmoney) {
        this.minusmoney = minusmoney;
    }
}